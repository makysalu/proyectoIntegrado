        UpeP es un partido político que se enmarca en la tradición constitucional nacida con la Ilustración, la Constitución norteamericana y la Revolución Francesa. Nuestro carácter ilustrado bebe directamente del liberalismo político español que estableció en las Cortes de Cádiz el concepto de Nación como la reunión del conjunto de los españoles, así como su carácter soberano. Una tradición que culmina con la Constitución democrática de 1978, garante de la división de poderes, la soberanía del pueblo español, de los principios de libertad, igualdad, solidaridad y justicia como valores supremos, del pluralismo político y del carácter social, democrático y aconfesional de nuestro Estado de Derecho.

En consecuencia, nos definimos como un partido liberal progresista, demócrata y constitucionalista.

Reivindicamos una sociedad abierta, libre de ingeniería social, donde los poderes públicos se dediquen a garantizar las libertades y derechos de los ciudadanos mediante el cumplimiento de las leyes democráticas. Una sociedad de ciudadanos libres e iguales que dispongan de las herramientas suficientes para desarrollar su potencial y buscar su felicidad. Para nosotros, en la sociedad abierta del siglo XXI, la libertad y la igualdad constituyen valores esenciales indisociables.

Propugnamos la defensa y el avance de las libertades individuales, sin que el lugar de nacimiento, la raza, el género, la orientación sexual, la religión, la opinión o cualquier otra circunstancia personal o social puedan limitarlas.

En UpeP creemos que recuperar y mantener la confianza del sistema democrático exige su permanente regeneración. Para ello, es condición esencial la efectiva división de poderes. Así mismo, un sistema electoral más moderno y más justo, la trasparencia y la democracia interna en los partidos políticos, y la libertad de expresión en los medios de comunicación, resultan indispensables en una democracia de calidad.

Es necesaria una nueva cultura política basada en el respeto al adversario, el rechazo a las actitudes políticas intolerantes y sectarias, así como el combate incesante contra la corrupción.

UpeP defiende una España unida y diversa que reconoce su historia, sus lenguas y su cultura como patrimonio de todos, sin hacer de esa riqueza lastre o arma política que ponga en riesgo nuestro futuro juntos.

Nos alineamos con el modelo territorial recogido en la Constitución española, el Estado de las autonomías. Para que este modelo perdure, deberán mejorarse la eficiencia y los mecanismos de coordinación de los poderes públicos, y garantizarse, en todo caso, la igualdad de todos los españoles.

UpeP es un partido europeísta. Somos partidarios de seguir avanzando en los distintos procesos de integración que desemboquen en la efectiva unión política de los más de 500 millones de ciudadanos que comparten el proyecto común europeo. El objetivo de este proceso de integración es la consecución de los Estados Unidos de Europa.

Así mismo, concebimos la comunidad internacional como un conjunto de Estados regido por las normas del Derecho internacional. Estamos comprometidos con el respeto y cumplimiento del marco normativo establecido por las Naciones Unidas, las libertades civiles, los derechos humanos, la seguridad, la lucha contra el terrorismo y la protección del medio ambiente. Asumimos el especial vínculo de nuestra nación con el conjunto de naciones iberoamericanas y todos sus ciudadanos. Apoyaremos toda iniciativa que contribuya a aumentar el peso específico de España en el mundo y la defensa y promoción de los intereses de los españoles.

Una clase media trabajadora fuerte y dinámica es condición esencial en una democracia de calidad. Aquella solo existirá en el marco de una economía de mercado donde los poderes públicos garanticen la libre competencia. Es la riqueza que genera este modelo económico la que podrá sostener la igualdad de oportunidades dentro de una sociedad de bienestar, y serán esos mismos poderes públicos los que deberán garantizar los derechos sociales reconocidos en nuestra constitución.

UpeP apuesta por la economía del conocimiento a través de la educación, la innovación, la ciencia y la cultura. Con un modelo económico social basado en el talento de nuestro capital humano pretendemos modernizar España, lograr que se genere empleo de calidad y ser más competitivos en la era de la globalización. Queremos, en definitiva, que emerja y se consolide la España de las oportunidades.

UpeP se compromete a actuar en política, tanto en las instituciones como en la sociedad, conforme a los valores expuestos en este ideario.
